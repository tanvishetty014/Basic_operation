public class BasicOperations {
    public static void main(String[] args) {
        int a = 15;
        int b = 5;
        int sum = a+b;
        int diff = a-b;
        int multiply = a*b;
        System.out.println("Addition: "+sum);
        System.out.println("Subtraction : "+diff);
        System.out.println("Multiplication : "+multiply);
    }
}
